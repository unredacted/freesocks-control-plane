// Generate verifier-extension/pinned.js from a built dist/ (CDN-blinding Phase 4).
// The pin is the SHA-384 of a NONCE-NORMALIZED dist/index.html: Caddy templates a
// per-request CSP nonce into `<meta name="csp-nonce">`, so the served index.html
// hash changes every request — we canonicalize that one tag before hashing, and
// the extension applies the IDENTICAL normalization to the served page. index.html
// carries SRI `integrity=` for every chunk (stable, content-addressed), so pinning
// its normalized hash transitively pins the whole executed bundle.
//
// Run AFTER a reproducible build, with the same VITE_FS_* env the build used:
//   bun run build && bun scripts/gen-verifier-pin.mjs
// (or `bun run verifier:pin`). See docs/oob-verification.md.
import { readFileSync, writeFileSync } from 'node:fs';
import { createHash } from 'node:crypto';

function loadEnv() {
  const env = { ...process.env };
  try {
    for (const line of readFileSync('.env.local', 'utf8').split('\n')) {
      const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*?)\s*$/);
      if (m && env[m[1]] === undefined) env[m[1]] = m[2].replace(/^["']|["']$/g, '');
    }
  } catch {
    /* no .env.local */
  }
  return env;
}

// MUST stay byte-identical to verifier-extension/background.js `normalizeIndexHtml`.
// Canonicalizes the per-request CSP-nonce meta so the pin is stable across requests.
function normalizeIndexHtml(html) {
  return html.replace(/<meta name="csp-nonce"[^>]*>/, '<meta name="csp-nonce" content="">');
}

const env = loadEnv();

let html;
try {
  html = readFileSync('dist/index.html', 'utf8');
} catch {
  console.error('dist/index.html not found — run `bun run build` first.');
  process.exit(1);
}
const indexSha384 = createHash('sha384').update(normalizeIndexHtml(html), 'utf8').digest('hex');

// Origin the extension watches (scheme + host, no path). From the deploy env, else
// the canonical default; the operator also sets it in verifier-extension/manifest.json.
function appOrigin() {
  const raw = env.PUBLIC_BASE_URL || env.VITE_PUBLIC_BASE_URL || env.APP_HOST;
  if (!raw) return 'https://app.freesocks.org';
  try {
    const u = new URL(raw.includes('://') ? raw : `https://${raw}`);
    return `${u.protocol}//${u.host}`;
  } catch {
    return 'https://app.freesocks.org';
  }
}
const origin = appOrigin();
const manifestPk = env.VITE_FS_MANIFEST_PK ?? '';
const manifestPkPq = env.VITE_FS_MANIFEST_PK_PQ ?? '';

const out = `// GENERATED by scripts/gen-verifier-pin.mjs — do not edit by hand.
// Pinned trust anchors for the FreeSocks bundle verifier. Ships INSIDE the
// store-reviewed extension (not from the CDN), so the CDN can't change it.
// Regenerate on every release: bun run build && bun run verifier:pin.
export const PINNED = {
  origin: ${JSON.stringify(origin)},
  indexSha384: ${JSON.stringify(indexSha384)},
  manifestPk: ${JSON.stringify(manifestPk)},
  manifestPkPq: ${JSON.stringify(manifestPkPq)},
};
`;
writeFileSync('verifier-extension/pinned.js', out);
console.log(`wrote verifier-extension/pinned.js`);
console.log(`  origin:      ${origin}`);
console.log(`  indexSha384: ${indexSha384}`);
if (!manifestPk) {
  console.warn('  WARN: VITE_FS_MANIFEST_PK unset — the manifest-key check will be disabled.');
}
